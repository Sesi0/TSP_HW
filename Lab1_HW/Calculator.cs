namespace Lab1_HW
{
    public class Calculator
    {
        public static double AddNumbers(double val1, double val2)
        {
            return val1 + val2;
        }

        public static double SubstractNumbers(double val1, double val2)
        {
            return val1 - val2;
        }

        public static double MultiplyNumbers(double val1, double val2)
        {
            return val1 * val2;
        }

        public static double DivideNumbers(double val1, double val2)
        {
            return val1 / val2;
        }
    }
}
